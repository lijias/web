<?php
//创建画布
$img=imagecreatetruecolor(200,200);
//创建颜色
$green=imagecolorallocate($img,0,255,0);
$blue=imagecolorallocate($img,0,0,255);
$red=imagecolorallocate($img,255,0,0);
$bai=imagecolorallocate($img,255,255,255);
//填充画布
imagefill($img,0,0,$bai);
//创建点
for ($i=1;$i<=10000;$i++){
imagesetpixel($img,rand(1,199),rand(1,199),$red);
}
//创建线
imageline($img,100,0,100,200,$blue);
imageline($img,0,100,200,100,$blue);
//创建空心矩形
imagerectangle($img,50,50,100,100,$blue);
//创建实心矩形
imagefilledrectangle($img,100,100,150,150,$blue);
//创建空心圆
imageellipse($img,150,50,100,100,$blue);
imagefilledellipse($img,150,50,50,50,$red);
//创建实心圆
imagefilledellipse($img,50,150,100,100,$red);
//创建文字
imagestring($img,5,132,42,'shit',$bai);
//创建文字2
imagettftext($img,10,45,50,160,$blue,"ariblk.ttf","holy");
//3.展示/保存
//通知浏览器输出图片
header("Content-Type:image/png");
//输出图片
imagepng($img);
//保存图片
imagepng($img,"shenniao.png");
//释放
imagedestroy($img);